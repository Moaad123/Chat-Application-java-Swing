    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String name =jTextField1.getText() ;
        String last_name = jTextField2.getText();
        String username = jTextField3.getText();
        String password = jTextField4.getText(); 
        try {
            Socket client= new Socket("localhost",4000);
            if(!name.isEmpty() && !last_name.isEmpty() && !username.isEmpty() && !password.isEmpty()){
                new Logiin_and_register(username,password,name,last_name,client);
                BufferedReader register= new BufferedReader(new InputStreamReader(client.getInputStream()));
                String cond =  register.readLine();
                if(cond.equals("exist")){
                    JOptionPane.showMessageDialog(null,"le nom d'utilisateur  existe deja choisi un autre","echec",JOptionPane.INFORMATION_MESSAGE);
                                        }
                if(cond.equals("succes")){
                    JOptionPane.showMessageDialog(null,"le compte est cree avec succee","succe",JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    new LoginForm1().setVisible(true);
                }
            }else{
                JOptionPane.showMessageDialog(null,"merci de remplir tout les champs","echec",JOptionPane.INFORMATION_MESSAGE);
                  }
        } catch (IOException ex) {
            Logger.getLogger(Register1.class.getName()).log(Level.SEVERE, null, ex);                          
        }
        
    }//GEN-LAST:event_jButton1MouseClicked
    
    

